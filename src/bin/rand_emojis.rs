struct EmojiIter {
    rng: ThreadRng,
}

impl EmojiIter {
    fn new() -> Self {
        Self {
            rng: rand::thread_rng(),
        }
    }
}
impl Iterator for EmojiIter {
    type Item = &'static str;
    fn next(&mut self) -> Option<Self::Item> {
        Some(EMOJIS[self.rng.gen_range(0..EMOJIS.len())])
    }
}

// parse str to usize
fn str_to_usize<T: AsRef<str>>(string: T) -> Result<usize, std::num::ParseIntError> {
    string.as_ref().parse::<usize>()
}

/// get argument 1 from command line
fn parse_args() -> usize {
    let args: Vec<String> = std::env::args().collect();

    if args.len() <= 1 {
        println!("Error: please provide the amount of random emojis you need 🗿");
        exit(1);
    }

    match str_to_usize(&args[1]) {
        Ok(n) => n,
        Err(_) => {
            println!("Error: please provide a valid number of random emojis you need 🗿");
            exit(1);
        }
    }
}

fn main() {
    let emojis_amount = parse_args();
    for emoji in EmojiIter::new().take(emojis_amount) {
        print!("{}", emoji);
    }
}
